package com.unitconverter.models;


public class Mass {

    private double milligram;
    private double gram;
    private double decagram;
    private double kilogram;
    private double ton;
    private double ounce;
    private double pound;

    public Mass() {
    }

    public double getMilligram() {
        return milligram;
    }

    public void setMilligram(double milligram) {
        this.milligram = milligram;
    }

    public double getGram() {
        return gram;
    }

    public void setGram(double gram) {
        this.gram = gram;
    }

    public double getDecagram() {
        return decagram;
    }

    public void setDecagram(double decagram) {
        this.decagram = decagram;
    }

    public double getKilogram() {
        return kilogram;
    }

    public void setKilogram(double kilogram) {
        this.kilogram = kilogram;
    }

    public double getTon() {
        return ton;
    }

    public void setTon(double ton) {
        this.ton = ton;
    }

    public double getOunce() {
        return ounce;
    }

    public void setOunce(double ounce) {
        this.ounce = ounce;
    }

    public double getPound() {
        return pound;
    }

    public void setPound(double pound) {
        this.pound = pound;
    }
}
